        private string _Header = "[LlamaSharpEngine] ";
        private LoggingModule _Logging = null;

        private LLamaWeights _Model = null;
        private LLamaContext _Context = null;
        private LLamaWeights _EmbeddingModel = null;
        private LLamaEmbedder _Embedder = null;
        private InteractiveExecutor _Executor = null;
        private StatelessExecutor _StatelessExecutor = null;
        private ChatSession _ChatSession = null;
        private bool _IsInitialized = false;
        private bool _Disposed = false;
        private int _EmbeddingDimensions = -1;
        private readonly SemaphoreSlim _EmbedderSemaphore = new SemaphoreSlim(1, 1);
        private readonly SemaphoreSlim _GenerationSemaphore = new SemaphoreSlim(1, 1);
        #endregion

        #region Constructors-and-Factories

        /// <summary>
        /// Initializes a new instance of the LlamaSharpEngine class.
        /// </summary>
        /// <param name="logging">Optional logging module for capturing debug and error information. If null, a new LoggingModule will be created.</param>
        public LlamaSharpEngine(LoggingModule logging = null)
        {
            _Logging = logging ?? new LoggingModule();

            _Logging.Debug(_Header + "initialized");
        }

        #endregion

        #region Public-Methods

        /// <summary>
        /// Releases all resources used by the LlamaSharpEngine.
        /// Properly disposes of the model, context, and embedder instances.
        /// </summary>
        public override void Dispose()
        {
            if (_Disposed) return;

            try
            {
                _ChatSession = null;
                _Executor = null;
                _StatelessExecutor = null;

                _Embedder?.Dispose();
                _EmbeddingModel?.Dispose();
                _Context?.Dispose();
                _Model?.Dispose();

                _EmbedderSemaphore?.Dispose();
                _GenerationSemaphore?.Dispose();
            }
            catch (Exception ex)
            {
                _Logging.Warn(_Header + "disposal exception:" + Environment.NewLine + ex.ToString());
            }
            finally
            {
                _Disposed = true;
                _IsInitialized = false;
            }
        }

        #region Initialization

        /// <summary>
        /// Initializes the LlamaSharp engine with the specified model file.
        /// Loads the model, creates context, and sets up executors for text generation and embeddings.
        /// </summary>
        /// <param name="modelPath">The file path to the GGUF model file to load.</param>
        /// <returns>A task that represents the asynchronous initialization operation.</returns>
        /// <exception cref="Exception">Thrown when the model fails to load or initialize.</exception>
        public override async Task InitializeAsync(string modelPath)
        {
            if (_IsInitialized) return;

            ModelPath = modelPath;

            await Task.Run(() =>
            {
                try
                {
                    int gpuLayers = GetOptimalGpuLayers();
                    _Logging.Debug(_Header + $"initializing LlamaSharp with {(gpuLayers != 0 ? "GPU" : "CPU")} {(gpuLayers != 0 ? $" ({gpuLayers} layers requested)" : "")}");

                    ModelParams parameters = CreateModelParams(modelPath, gpuLayers, false);

                    _Model = LLamaWeights.LoadFromFile(parameters);
                    _Context = _Model.CreateContext(parameters);

                    _Executor = new InteractiveExecutor(_Context); // text generation
                    _StatelessExecutor = new StatelessExecutor(_Model, parameters); // text generation
                    _ChatSession = new ChatSession(_Executor);

                    // For embeddings, try to create a separate instance
                    try
                    {
                        ModelParams embeddingParams = CreateModelParams(modelPath, gpuLayers, true);

                        _EmbeddingModel = LLamaWeights.LoadFromFile(embeddingParams);
                        _Embedder = new LLamaEmbedder(_EmbeddingModel, embeddingParams);
                    }
                    catch (Exception ex)
                    {
                        _Logging.Warn(_Header + "failed to initialize embeddings:" + Environment.NewLine + ex.ToString());
                        _Embedder = null;
                    }

                    _IsInitialized = true;

                    _Logging.Debug(_Header + "LlamaSharp provider initialized successfully");
                }
                catch (Exception ex)
                {
                    throw new Exception("failed to initialize LlamaSharp:" + Environment.NewLine + ex.ToString());
                }
            });
        }

        /// <summary>
        /// Determines the optimal number of GPU layers to use based on available hardware.
        /// </summary>
        /// <returns>The number of GPU layers to use, or 0 if GPU acceleration is not available. Returns -1 to use all available GPU layers.</returns>
        public override int GetOptimalGpuLayers()
        {
            try
            {
                // Check which backend was selected by NativeLibraryBootstrapper
                string selectedBackend = SharpAI.Classes.Runtime.NativeBackendInfo.SelectedBackend;

                if (selectedBackend.Equals("cuda", StringComparison.OrdinalIgnoreCase)
                    || selectedBackend.Equals("metal", StringComparison.OrdinalIgnoreCase))
                {
                    long gpuDeviceCount = LLama.Native.NativeApi.llama_max_devices();
                    _Logging.Debug(_Header + $"{selectedBackend} backend selected, {gpuDeviceCount} GPU device(s) available");

                    string configuredGpuLayers = SharpAIEnvironment.GetString(SharpAIEnvironment.GpuLayers);
                    if (!String.IsNullOrEmpty(configuredGpuLayers)
                        && !configuredGpuLayers.Equals("auto", StringComparison.OrdinalIgnoreCase))
                    {
                        if (Int32.TryParse(configuredGpuLayers, out int parsedGpuLayers) && parsedGpuLayers >= -1)
                        {
                            if (parsedGpuLayers == -1)
                            {
                                _Logging.Debug(_Header + "GPU layer override requests all layers");
                                return 999;
                            }

                            _Logging.Debug(_Header + $"GPU layer override requests {parsedGpuLayers} layer(s)");
                            return parsedGpuLayers;
                        }

                        _Logging.Warn(_Header + $"{SharpAIEnvironment.GpuLayers} value '" + configuredGpuLayers + "' is invalid, using automatic GPU layer selection");
                    }

                    return 999; // big positive -> clamp to all layers
                }
                else
                {
                    _Logging.Debug(_Header + $"{selectedBackend} backend selected, using CPU");
                    return 0;
                }
            }
            catch (Exception ex)
            {
                _Logging.Debug(_Header + "GPU detection exception, using CPU:" + Environment.NewLine + ex.ToString());
                return 0;
            }
        }

        #endregion

        #region Runtime-Parameters

        private ModelParams CreateModelParams(string modelPath, int gpuLayers, bool embeddings)
        {
            int threads = GetConfiguredThreadCount();
            int batchThreads = SharpAIEnvironment.GetInt(SharpAIEnvironment.BatchThreads, threads, 1);
            int mainGpu = SharpAIEnvironment.GetInt(SharpAIEnvironment.MainGpu, -1, 0);
            uint contextSize = SharpAIEnvironment.GetUInt(SharpAIEnvironment.ContextSize, 0, 0);
            uint batchSize = SharpAIEnvironment.GetUInt(SharpAIEnvironment.BatchSize, 0, 0);
            uint uBatchSize = SharpAIEnvironment.GetUInt(SharpAIEnvironment.UBatchSize, 0, 0);
            bool useMmap = SharpAIEnvironment.GetBool(SharpAIEnvironment.UseMmap, true);
            bool useMlock = SharpAIEnvironment.GetBool(SharpAIEnvironment.UseMlock, false);
            bool flashAttention = SharpAIEnvironment.GetBool(SharpAIEnvironment.FlashAttention, false);

            ModelParams parameters = new ModelParams(modelPath)
            {
                GpuLayerCount = gpuLayers,
                Threads = threads,
                BatchThreads = batchThreads,
                Embeddings = embeddings,
                UseMemorymap = useMmap,
                UseMemoryLock = useMlock,
                FlashAttention = flashAttention
            };

            if (mainGpu >= 0) parameters.MainGpu = mainGpu;
            if (contextSize > 0) parameters.ContextSize = contextSize;
            if (batchSize > 0) parameters.BatchSize = batchSize;
            if (uBatchSize > 0) parameters.UBatchSize = uBatchSize;

            _Logging.Debug(_Header + "model params: "
                + "threads=" + threads
                + ", batchThreads=" + batchThreads
                + ", gpuLayers=" + gpuLayers
                + ", mainGpu=" + (mainGpu >= 0 ? mainGpu.ToString() : "auto")
                + ", contextSize=" + (contextSize > 0 ? contextSize.ToString() : "model-default")
                + ", batchSize=" + (batchSize > 0 ? batchSize.ToString() : "default")
                + ", uBatchSize=" + (uBatchSize > 0 ? uBatchSize.ToString() : "default")
                + ", useMmap=" + useMmap
                + ", useMlock=" + useMlock
                + ", flashAttention=" + flashAttention
                + ", embeddings=" + embeddings);

            return parameters;
        }

        private int GetConfiguredThreadCount()
        {
            int defaultThreads = Math.Max(1, Environment.ProcessorCount - 1);
            return SharpAIEnvironment.GetInt(SharpAIEnvironment.NumThreads, defaultThreads, 1);
        }

