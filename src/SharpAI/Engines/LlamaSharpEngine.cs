        #region Public-Members

        /// <summary>
        /// Boolean indicating if the engine is disposed.
        /// </summary>
        public override bool IsDisposed
        {
            get
            {
                return _Disposed;
            }
        }
        
        /// <summary>
        /// Gets the number of dimensions in the embedding vectors generated by this engine.
        /// Returns -1 if not yet determined, 0 if embeddings are not supported.
        /// </summary>
        public override int EmbeddingDimensions
        {
            get
            {
                if (_EmbeddingDimensions == -1 && _Embedder != null)
                {
                    // Get embedding dimensions by testing with a small input
                    try
                    {
                        IReadOnlyList<float[]> testEmbeddings = _Embedder.GetEmbeddings("test").Result;
                        float[] testEmbedding = testEmbeddings.Single();
                        _EmbeddingDimensions = testEmbedding.Length;
                    }
                    catch
                    {
                        _EmbeddingDimensions = 0;
                    }
                }
                return _EmbeddingDimensions;
            }
        }

        /// <summary>
        /// Gets a value indicating whether this engine supports GPU acceleration.
        /// LlamaSharp supports GPU acceleration when CUDA is available.
        /// </summary>
        public override bool SupportsGpu => true;

        /// <summary>
        /// Gets a value indicating whether this engine supports generating embeddings.
        /// Determined by inspecting the GGUF architecture and pooling-type metadata.
        /// </summary>
        public override bool SupportsEmbeddings => DetectSupportsEmbeddings();

        /// <summary>
        /// Gets a value indicating whether this engine supports text generation.
        /// Determined by inspecting the GGUF architecture metadata. Pure embedding
        /// architectures (BERT family, T5 encoder, etc.) return false.
        /// </summary>
        public override bool SupportsGeneration => DetectSupportsGeneration();

        /// <summary>
        /// Gets the GGUF <c>general.architecture</c> value from the loaded model, or null if unavailable.
        /// </summary>
        public string Architecture
        {
            get
            {
                if (_Model == null || _Model.Metadata == null) return null;
                if (_Model.Metadata.TryGetValue("general.architecture", out string arch)) return arch;
                return null;
            }
        }

        /// <summary>
        /// Gets whether this engine has been successfully initialized.
        /// </summary>
        public override bool IsInitialized => _IsInitialized;

        /// <summary>
        /// Gets the context window size (maximum number of tokens) for this model.
        /// </summary>
        /// <returns>The context window size in tokens, or -1 if not available.</returns>
        public override int GetContextSize()
        {
            if (!_IsInitialized || _Context == null)
                return -1;

            return (int)_Context.ContextSize;
        }

        /// <summary>
        /// Count tokens in <paramref name="text"/> using the provider's tokenizer.
        /// </summary>
        /// <param name="text">Input text to tokenize.</param>
        /// <param name="addBos">Whether to include a beginning-of-sentence token.</param>
        /// <param name="parseSpecial">Whether to parse special tokens (e.g., &lt;|im_start|&gt;).</param>
        /// <returns>Total token count; 0 if the engine is not initialized.</returns>
        public override int CountTokens(string text, bool addBos = true, bool parseSpecial = true)
        {
            if (!_IsInitialized || _Context == null || string.IsNullOrEmpty(text))
                return 0;
            try
            {
                LLamaToken[] toks = _Context.Tokenize(text, addBos, parseSpecial);
                return toks.Length;
            }
            catch
            {
                return Math.Max(1, text.Length / 4);
            }
        }

